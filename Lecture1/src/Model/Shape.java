package Model;

public class Shape {
	String shape;
	int x_click;
	int y_click;
	int x_drop;
	int y_drop;
	
	public String getShape() {
		return shape;
	}
	public void setShape(String shape) {
		this.shape = shape;
	}
	public int getX_click() {
		return x_click;
	}
	public void setX_click(int x_click) {
		this.x_click = x_click;
	}
	public int getY_click() {
		return y_click;
	}
	public void setY_click(int y_click) {
		this.y_click = y_click;
	}
	public int getX_drop() {
		return x_drop;
	}
	public void setX_drop(int x_drop) {
		this.x_drop = x_drop;
	}
	public int getY_drop() {
		return y_drop;
	}
	public void setY_drop(int y_drop) {
		this.y_drop = y_drop;
	}

	}

